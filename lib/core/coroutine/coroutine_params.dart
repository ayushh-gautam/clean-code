class WithOneParam {
  final dynamic function;
  final dynamic param;

  WithOneParam(this.function, this.param);
}

class WithTwoParam {
  final dynamic function;
  final dynamic param1, param2;

  WithTwoParam(
    this.function,
    this.param1,
    this.param2,
  );
}

class WithThreeParam {
  final dynamic function;
  final dynamic param1, param2, param3;

  WithThreeParam(
    this.function,
    this.param1,
    this.param2,
    this.param3,
  );
}

class WithFourParam {
  final dynamic function;
  final dynamic param1, param2, param3, param4;

  WithFourParam(
    this.function,
    this.param1,
    this.param2,
    this.param3,
    this.param4,
  );
}
